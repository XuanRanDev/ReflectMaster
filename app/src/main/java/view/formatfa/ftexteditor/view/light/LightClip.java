package view.formatfa.ftexteditor.view.light;

public class LightClip {
    //代码高亮块
    private int start;
    private int end;
    private int color;

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }
}
